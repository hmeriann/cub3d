#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*buf_s;

	i = 0;
	buf_s = s;
	while (i < n)
	{
		buf_s[i] = '\0';
		i++;
	}
}
