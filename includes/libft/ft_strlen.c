#include <stddef.h>
#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	if (s == NULL)
		return (0);
	while (s[i])
	{
		i++;
		n++;
	}
	return (n);
}
